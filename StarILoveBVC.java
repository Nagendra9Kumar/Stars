public class StarILoveBVC {
    public static void main(String args[]) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 30; j++) {
                if (
                // I
                (i == 1 && (j >= 1 && j <= 5))
                        || (i == 5 && (j >= 1 && j <= 5))
                        || (j == 3)
                        // ❤️
                        || (i == 1 && (j == 8 || j == 10))
                        || (i == 2 && (j == 7 || j == 11 || j == 9))
                        || (i == 3 && (j == 7 || j == 11))
                        || (i == 4 && (j == 8 || j == 10))
                        || (i == 5 && (j == 9))
                        // B
                        || (j == 13)
                        || (i == 1 && (j >= 13 && j <= 16))
                        || (i == 3 && (j >= 13 && j <= 16))
                        || (i == 5 && (j >= 13 && j <= 16))
                        || (i == 4 && j == 17)
                        || (i == 2 && j == 17)
                        // V
                        || (j == 19 && i < 4)
                        || (j == 23 && i < 4)
                        || (i == 5 && j == 21)
                        || (i == 4 && (j == 20 || j == 22))
                        // C
                        || ((i > 1 && i < 5) && j == 25)
                        || (i == 1 && (j > 25 && j < 30))
                        || (i == 5 && (j > 25 && j < 30))) {
                    System.out.print("* ");
                } else
                    System.out.print("  ");
            }
            System.out.println("");
        }
    }
}
